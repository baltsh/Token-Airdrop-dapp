let contractToken;
let contractAirDrop;
let signer;

const provider = new ethers.providers.Web3Provider(window.ethereum, 80001); 

provider.send("eth_requestAccounts", []).then(() => {
    provider.listAccounts().then((accounts) => {
        signer = provider.getSigner(accounts[0]);
        console.log(signer);
        contractToken = new ethers.Contract(
            contractQBCoinAddress,
            contractQBCoinTokenABI,
            signer
        );
		

        contractAirDrop = new ethers.Contract(
            contractAirDropAddress,
            contractAirDropABI,
            signer
        );
    });
});

async function getBalance() {
    let ERC20Balance = await contractToken.balanceOf(signer.getAddress());
	console.log(signer.getAddress());
	console.log(ERC20Balance);
    document.getElementById("balance").innerText = ERC20Balance;
}

async function airdropTokensWithTransfer() {
    var options = document.getElementById('addresses').selectedOptions;
	var values = Array.from(options).map(({ value }) => value);
	console.log(values);  
	await  contractAirDrop.airdropTokensWithTransferToArray(values, 1);
}